    public void itemCost() {
        double sum = 0;

        for (int i = 0; i < tbl.getRowCount(); i++) {
            double quantity = Double.parseDouble(tbl.getValueAt(i, 2).toString()); // Assuming column 3 contains the quantity
            double price = Double.parseDouble(tbl.getValueAt(i, 3).toString()); // Assuming column 4 contains the unit price
            sum += quantity * price;
        }

        double ctotal = sum;
        double cTax = (ctotal * 15) / 100;

        String iTaxTotal = String.format("Rs%.2f", cTax);
        taxBox.setText(iTaxTotal);

        String isubTotal = String.format("Rs%.2f", ctotal);
        subTotBox.setText(isubTotal);

        String iTotal = String.format("Rs%.2f", ctotal + cTax);
        totalBox.setText(iTotal);
    }

