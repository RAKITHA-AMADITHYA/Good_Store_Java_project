    public void setItemCode() throws SQLException, ClassNotFoundException{
          ResultSet rst = DBConnection.getInstance().getConnection().prepareStatement("SELECT * FROM Item").executeQuery();
          ArrayList<String> codes=new ArrayList<>();
          while(rst.next()){
               codes.add(rst.getString(1));
          }
          for(String x : codes){
          cmbItemCode.addItem(x);
          }
    }
    
        private void setOrderId() {
        try {
            String lastOrderId = OrderController.getLastOrderId();
            if (lastOrderId != null) {
                lastOrderId = lastOrderId.split("[A-Z]")[1]; // D001==> 001
                System.out.println(lastOrderId);
                lastOrderId = String.format("D%03d", (Integer.parseInt(lastOrderId) + 1));
                ORDIDBOX.setText(lastOrderId);
            } else {
                ORDIDBOX.setText("D001");
            }
        } catch (SQLException | ClassNotFoundException e) {
            e.printStackTrace();
        }
    }
